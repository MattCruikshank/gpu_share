/// Returns all pending input events as length-prefixed protobuf bytes.
/// Format: [u32le length][protobuf bytes][u32le length][protobuf bytes]...
/// TypeScript decodes each chunk using protobuf-es InputEvent.fromBinary().
#[op2]
#[buffer]
fn op_gpu_poll_events(state: &mut OpState) -> Vec<u8> {
    let gpu = state.borrow_mut::<Arc<Mutex<GpuState>>>();
    let mut gpu = gpu.lock().unwrap();
    let events = std::mem::take(&mut gpu.input_events);
    let mut buf = Vec::new();
    for ev_bytes in &events {
        let len = ev_bytes.len() as u32;
        buf.extend_from_slice(&len.to_le_bytes());
        buf.extend_from_slice(ev_bytes);
    }
    buf
}

#[op2(fast)]
fn op_gpu_set_webgpu_active(state: &mut OpState, active: bool) {
    let gpu = state.borrow_mut::<Arc<Mutex<GpuState>>>();
    let mut gpu = gpu.lock().unwrap();
    gpu.webgpu_active = active;
}

#[op2(fast)]
fn op_log(#[string] msg: &str) {
    eprintln!("[scene] {}", msg);
}

